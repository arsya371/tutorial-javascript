/** Operator Aritmatika */

// Operasi Penjumlahan +

let x = 5
let y = 10
let sum = x + y // output: 15
// console.log(sum, typeof sum)

// Operasi Pengurangan -
let a = 7
let b = 3
let diff = a - b // output: 4
// console.log(diff, typeof diff)

// Operasi Perkalian *
let p = 5
let q = 6
let result = p * q // output: 30
// console.log(result)

// Operasi Pembagian /
let m = 100
let n = 4
let bagi = m / n // output: 25
// console.log(bagi)

// Operasi Modulus %
let r = 17
let s = 4 // 16 4 4
let mod = r % s // output: 3
// console.log(mod)

// Operasi Eksponen **
let base = 2
let exponent = 3
let power = base ** exponent // output: 8
// console.log(power)

// Operator Increment 
let count = 5
// count++ // output: 6
count = count + 1 // output: 6
// console.log(count)

// Operator Decrement
let countDown = 5
// countDown-- // output: 4
countDown = countDown - 1 // output: 4
// console.log(countDown)